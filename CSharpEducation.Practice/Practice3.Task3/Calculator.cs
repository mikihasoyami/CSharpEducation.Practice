using System;
using System.Collections.Generic;
using System.Text;

namespace Practice3.Task3
{
    public class Calculator
    {
        public double Plus(double FirstVlue, double SecondVlue)
        {
            return FirstVlue + SecondVlue;
        }

        public double Minus(double FirstVlue, double SecondVlue)
        {
            return FirstVlue - SecondVlue;
        }

        public double Multi(double FirstVlue, double SecondVlue)
        {
            return FirstVlue * SecondVlue;
        }

        public double Div(double FirstVlue, double SecondVlue)
        {
            return FirstVlue / SecondVlue;
        }
    }
}
